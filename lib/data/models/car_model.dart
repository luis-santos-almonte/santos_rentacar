class Car {
  final String model;
  final double distance;
  final double fuelCapacity;
  final String fuelType;
  final double pricePerHour;
  final double year;
  final String color;

  Car(
      {required this.model,
      required this.distance,
      required this.color,
      required this.fuelCapacity,
      required this.fuelType,
      required this.pricePerHour,
      required this.year});
}
